package com.utilities;

public class SimPiece {

	private int x_num;
	private int y_num;
	
	public SimPiece(int x_num, int y_num) {
		this.x_num = x_num; 
		this.y_num = y_num; 
	}

	public int getX_num() {
		return x_num;
	}

	public void setX_num(int x_num) {
		this.x_num = x_num;
	}

	public int getY_num() {
		return y_num;
	}

	public void setY_num(int y_num) {
		this.y_num = y_num;
	}

}
